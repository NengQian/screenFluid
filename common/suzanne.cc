glm::vec3 *suzanne_normals = nullptr;

void suzanne_calc_normals() {
    if (suzanne_normals) return;

    int vc = sizeof(suzanne_vertices) / sizeof(suzanne_vertices[0]);
    int ic = sizeof(suzanne_indices) / sizeof(suzanne_indices[0]);
    suzanne_normals = new glm::vec3[vc / 3];

    for (int i = 0; i < vc / 3; ++i)
        suzanne_normals[i] = glm::vec3(0, .00001, 0);

    glm::vec3 *verts = (glm::vec3*)suzanne_vertices;
    for (int i = 0; i < ic; i += 3) {
        int i0 = suzanne_indices[i + 0];
        int i1 = suzanne_indices[i + 1];
        int i2 = suzanne_indices[i + 2];

        glm::vec3 p0 = verts[i0];
        glm::vec3 p1 = verts[i1];
        glm::vec3 p2 = verts[i2];

        auto v0 = p1 - p0;
        auto v1 = p2 - p0;
        auto n = cross(v0, v1);

        suzanne_normals[i0] += n;
        suzanne_normals[i1] += n;
        suzanne_normals[i2] += n;
    }

    for (int i = 0; i < vc / 3; ++i)
        suzanne_normals[i] = glm::normalize(suzanne_normals[i]);
}

